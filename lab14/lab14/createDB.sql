USE univer_db;
GO

CREATE TABLE FACULTY (
    FACULTY NVARCHAR(50) PRIMARY KEY,
    FACULTY_NAME NVARCHAR(100) NOT NULL
);

CREATE TABLE PULPIT (
    PULPIT NVARCHAR(50) PRIMARY KEY,
    PULPIT_NAME NVARCHAR(100) NOT NULL,
    FACULTY NVARCHAR(50) NOT NULL,
    FOREIGN KEY (FACULTY) REFERENCES FACULTY(FACULTY)
);

CREATE TABLE TEACHER (
    TEACHER NVARCHAR(50) PRIMARY KEY,
    TEACHER_NAME NVARCHAR(100) NOT NULL,
    PULPIT NVARCHAR(50) NOT NULL,
    FOREIGN KEY (PULPIT) REFERENCES PULPIT(PULPIT)
);

CREATE TABLE SUBJECT (
    SUBJECT NVARCHAR(50) PRIMARY KEY,
    SUBJECT_NAME NVARCHAR(100) NOT NULL,
    PULPIT NVARCHAR(50) NOT NULL,
    FOREIGN KEY (PULPIT) REFERENCES PULPIT(PULPIT)
);

CREATE TABLE AUDITORIUM_TYPE (
    AUDITORIUM_TYPE NVARCHAR(50) PRIMARY KEY,
    AUDITORIUM_TYPENAME NVARCHAR(100) NOT NULL
);

CREATE TABLE AUDITORIUM (
    AUDITORIUM NVARCHAR(50) PRIMARY KEY,
    AUDITORIUM_NAME NVARCHAR(100) NOT NULL,
    AUDITORIUM_CAPACITY INT NOT NULL,
    AUDITORIUM_TYPE NVARCHAR(50) NOT NULL,
    FOREIGN KEY (AUDITORIUM_TYPE) REFERENCES AUDITORIUM_TYPE(AUDITORIUM_TYPE)
);



